# 1.OPERATORS

#Sample Variable
a = 10
b = 20
x = True
y = False

print(f" a = {a}, b = {b}, x = {x}, y = {y}")

#Arithmatic Operations
print(f"a + b = {a+b}")
print(f"a - b = {a-b}")
print(f"a / b = {a/b}")
print(f"a * b = {a*b}")
print(f"a % b = {a%b}")
print(f"a ** b = {a**b}")
print(f"a // b = {a//b}")

